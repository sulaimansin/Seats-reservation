package project2;

/**
 *
 * @author Sulaiman
 */
public class lecturer {

    private  int id;
    private String name;

    public lecturer(){
        this(0 , "");
    }

    public lecturer(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "lecturer{" + "id=" + id + ", name=" + name + '}';
    }
    
  

}

